# цүнхний багтаамж (capacity) болон жингийн жагсаалт (weights) ба үнэ цэнүүдийн жагсаалт (values) өгөгднө.
# Зорилго бол эдгээр зүйлсийн жинг болон үнэ цэнийг харгалзан үзэж, цүнхэнд хамгийн их үнэ цэнийг хэрхэн хуулах вэ гэдгийг олох явдал юм.
# Энэ бодлого нь "Fractional Knapsack" буюу "Хэсэгчлэн авах" асуудал бөгөөд цүнхэнд багтахгүй бол зөвхөн хэрэгтэй хэсгийг нь авч болно.

# Алгоритмын тайлбар:
# 1. **Жагсаалтыг эрэмбэлэх:**
#    - Эхлээд бид бүх зүйлсийн жингийн харьцаа (value/weight) болон жингээр нь эрэмбэлнэ.
#    - Эдгээрийн жингийн харьцаагаар хамгийн өндөр ашигтай, бага жинтэй элементүүдийг эхлээд сонгоно.
#    - Ингэснээр хамгийн ашигтай зүйлсийг хамгийн түрүүнд авч, хамгийн их ашиг олох боломжтой.

# 2. **Эрдэмтэдийг сонгох:**
#    - Дараа нь бид жингийн харьцаагаар эрэмбэлсэн жагсаалтаар дамжиж, эхлээд хамгийн их ашигтай зүйлсийг авна.
#    - Хэрэв цүнхний багтаамж нь тухайн зүйлсийг авахад хангалттай бол бүтнээр нь авч болно.
#    - Энэ үед багтаамж дуусах хүртэл бүх зүйлсийг бүрэн авч чадна.

# 3. **Багтаамжийн хязгаар:**
#    - Хэрэв цүнхний багтаамж нь бүх зүйлийг хүлээж авахгүй бол бид үлдсэн багтаамжийг хамгийн сүүлд үлдсэн зүйлсийг хэмжээгээр нь авч болно.
#    - Үүний үр дүнд хамгийн их үнэ цэнийг авахын тулд хамгийн хямд үнэ цэнэтэй зүйлсийн хэсгийг авах болно.

# 4. **Үнийн нэмэгдэл:**
#    - Эцэст нь, бүх зүйлсийг авах боломжгүй бол хамгийн сүүлд үлдсэн хэсэг болохыг хувиар нь авч, хамгийн их боломжит үнэ цэнийг хуримтлуулна.
#    - Үүнийг хийгээд хамгийн их ашигтай шийдлийг гаргана.

def fractional_knapsack(weights, values, capacity):

    items = sorted(zip(weights, values), key=lambda x: x[1] / x[0], reverse=True)

    max_value = 0
    for weight, value in items:
        if capacity >= weight:
            max_value += value
            capacity -= weight
        else:
            max_value += value * (capacity / weight)
            break
    return max_value



weights = [10, 20, 30]
values = [60, 100, 120]
capacity = 50

result = fractional_knapsack(weights, values, capacity)
print("Хамгийн их үнэ цэнэ:", result)
